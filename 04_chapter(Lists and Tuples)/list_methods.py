#SORTING IN ASCENDING ORDER
l1 = [1,5,6,3,8,2,4,9,7]
l1.sort()
print(l1)

#SETTING LIST IN REVERSE ORDER
l2 = [1,5,6,3,8,2,4,9,7]
l2.sort()
l2.reverse()
print(l2)

#APPEND(add at the end of the list)
l1.append(10)
print(l1)

#INSERT(insert at the specific index)
l1.insert(2,'&')
print(l1)

#POP(delete item at specific index)
l1.pop(2)
print(l1)

#REMOVE(removes specific item from the list)
l1.remove(10)
print(l1)

